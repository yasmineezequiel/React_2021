// Destructuring is used to extract elements or objects properties and re-assign them into multiple variables. Example:

const oldArray = ["Hello", 1, { name: "Foo" }];
[a, b, c] = oldArray;
console.log(a); // Hello
console.log(b); // 1
console.log(c); // {name: 'Foo'}

